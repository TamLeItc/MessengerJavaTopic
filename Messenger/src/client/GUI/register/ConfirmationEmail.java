    //Tạo ra 1 số ngẫu nhiên từ 1001 - 9999
    public String randomCode(){
        Random rand = new Random();
        while(true){
            int temp = rand.nextInt() % 10000;
            if(temp > 1000){
                return temp + "";
            }
        }
    }
    
    public void sendConfirmCode(){
        _confirmCode = randomCode();
        String message = "Your email confirm code TalkTalk is: <h3>" + _confirmCode + "</h3>"
                + "never share this code for anyone";
        EmailSender.sendMail(this._email, message);
    }

