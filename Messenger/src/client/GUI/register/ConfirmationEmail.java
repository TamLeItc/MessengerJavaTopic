    private void jBtnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmActionPerformed
        if(checkInfor() == false){
            JOptionPane.showMessageDialog(null, "Confirm code is invalid", "Error", JOptionPane.ERROR_MESSAGE);
            this.jTxtConfirmCode.setText("");
        }
        else{
            Client.USER.setEmail(FrmProfile.JTXT_EMAIL.getText());
            Client.CLIENT.updateUser();

            FrmProfile.JLBL_SAVE_EMAIL.setText("         ");
            CHANGE_FOREGROUD_LABEL(FrmProfile.JLBL_CANCEL_EMAIL, "GRAY");
            FrmProfile.JLBL_CANCEL_EMAIL.setText("   Public");
            JTXT_GENDER.setEditable(false);
            FrmProfile.JTXT_EMAIL.setBorder(null);
            
            this.setVisible(false);
        }
    }//GEN-LAST:event_jBtnConfirmActionPerformed
    
    public boolean checkInfor(){
        if(this.jTxtConfirmCode.getText().equals(_confirmCode)){
            return true;
        }
        else{
            JOptionPane.showMessageDialog(null, "Confirm code is invalid", "Error", JOptionPane.ERROR_MESSAGE);
            this.jTxtConfirmCode.setText("");
            return false;
        }
    }

