    public void handleRegister(){
        load.setVisible(false);
        this.jPnlMain.setVisible(true);
        switch (_panelNumber) {
            case 1:
                this.jPnlContent.removeAll();
                jPnlContent.revalidate();
                this.jPnlContent.repaint();
                this.jPnlContent.add(acountInfo);
                acountInfo.setBounds(0, 0, 369, 345);
                acountInfo.setVisible(true);
                break;
            case 2:
                this.jPnlContent.removeAll();
                jPnlContent.revalidate();
                this.jPnlContent.repaint();
                this.jPnlContent.add(confirEmail);
                confirEmail.setBounds(0, 0, 369, 345);
                confirEmail.setVisible(true);
                break;
            case 3:
                this.jPnlContent.removeAll();
                jPnlContent.revalidate();
                this.jPnlContent.repaint();
                this.jPnlContent.add(personalInfo);
                personalInfo.setBounds(0, 0, 369, 345);
                personalInfo.setVisible(true);
                break;
            case 4:
                this.jPnlContent.removeAll();
                jPnlContent.revalidate();
                this.jPnlContent.repaint();
                this.jPnlContent.add(registerSuccess);
                registerSuccess.setBounds(0, 0, 369, 345);
                registerSuccess.setVisible(true);
                
                //Ẩn hai button
                this.jBtnBack.setVisible(false);
                this.jBtnNext.setVisible(false);
                this.jPnlMain.setSize(605, 605);
                
                
                break;
            default:
                break;
        }
    }
    
