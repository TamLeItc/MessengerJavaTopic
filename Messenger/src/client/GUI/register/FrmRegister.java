    private void jBtnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBackActionPerformed

        if(this._panelNumber == 1){
            return;
        }
        _panelNumber--;
        handleRegister();
    }//GEN-LAST:event_jBtnBackActionPerformed

    private void jBtnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnNextActionPerformed
        
        //Hiển thị form loading
        //Ẩn form đăng kí tài khoản
        jPnlMain.setVisible(false);
        load.setVisible(true);
        
        boolean check = true;
        switch (_panelNumber) {
            case 1:
                if(!acountInfo.checkInfo()){
                    check = false;
                    break;
                }   
                acountInfo.setAccountInformation();
                
                //Gửi mã xác nhận tới email đăng kí trước khi load Frame xác nhận email
                this.confirEmail = new ConfirmationEmail(FrmRegister.EMAIL);
                confirEmail.sendConfirmCode();
                
                break;
            case 2:
                if(!confirEmail.checkInfor()){
                    check = false;
                    break;
                }
                break;
            case 3:
                if(!personalInfo.checkInfo()){
                    check = false;
                    break;
                }   
                personalInfo.setAccountInformation();
                
                //Thực hiện việc đăng kí tài khoản
                //Xét avartar mặc định cho user. Nam và nữ có avartar mặc định khác nhau
                if(GENDER.equals("Male")){
                    PATH_AVARTAR = "images/avartar/boy.jpg";
                }
                else{
                    PATH_AVARTAR = "images/avartar/girl.png";
                }
                
                User user = new User(FIRST_NAME, LAST_NAME, BIRTH_DATE, GENDER, USER_NAME, PASSWORD, EMAIL, PATH_AVARTAR);
                String sql = new UserDAO().getStringInsertUser(user);
                Client.CLIENT.insertUser(sql);
                
                break;            
        }
        
        //Hiển thị form đăng kí tài khoản
        //ẩn form loading
        jPnlMain.setVisible(true);
        load.setVisible(false);
        if(check == true){
            _panelNumber++;
            handleRegister();
        }
    }//GEN-LAST:event_jBtnNextActionPerformed

